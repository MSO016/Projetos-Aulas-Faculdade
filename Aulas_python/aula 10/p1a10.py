# Escreva e rode o programa abaixo que imprime os números de 1 a 5.

for i in range(1, 6):
    print(i)

# Comente dentro do programa:
# -qual a função do for: ele executa uma variavel varias vezes.
# -o que é o "i": é uma Variavel 
# -o que significa range(1,6): range vai fazer o programa seguir uma sequencia do 1 a 6.