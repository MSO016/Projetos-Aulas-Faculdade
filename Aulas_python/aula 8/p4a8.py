numero = float(input("Digite um número: "))# este dará uma variavel "numero" 

if numero < 0: #este ira dizer se o numero é menor que 0
    print("O número é negativo.")#aqui dará a resposta digitada para o usuario do if
elif numero > 0:#aqui dirá se o numero é maior que 0
    print("O número é positivo.")#aqui dará a resposta digitada para o usuario do elif
else:#se não se encaixar em nenhumas das duas condiçoes if, elif
    print("O número é zero.")#aqui dará a resposta digitada para o usuario do else